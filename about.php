               <H1>ABOUT LUCKNOW'S CULTURE</H1>
<P>The culture of Uttar Pradesh swears by its rich historical past with its roots from Urdu. It is believed to be the land of the Nawabs, of Rama and Buddha, of Taj Mahal. It boasts of a mÃ©lange of vivid culture and architectural finesse and is synonymous with vibrant hues, delectable cuisine, timeless traditions, monumental heritage and astounding music. People of Uttar Pradesh take pride to be the first to introduce the two great epics- Ramayan and Mahabharat to the world. The state also has a plethora of fairs and festivals that add to the charm and charisma of the state.
Uttar Pradesh culture is an amalgam of a wide range of folk art and dance forms from which you may have your pick. Raslila is one of the most popular performing arts of the state. It owes its source to the drama act of Lord Krishna and his milkmaids. The folk tradition includes a variety of classical music, dance and drama. Ramlila is another famous art form, which is based on Lord Rama's life story.

Khayal is a form of semi-classical singing which comes from the royal courts of Awadh. Other than that, Svanga is a dance drama of semi-historical tales and ballads. Uttar Pradesh is home to a variety of cuisines, most famous of which are Awadhi cuisines and Mughal cuisines, very popular in Lucknow. The delicious food is supposed to be hereditary of the ruling Mughals. The most sought-after dishes include - dum biryani, dum Gosht and Nihari, which is slow-cooked overnight and sealed in large pots. In the northern Uttar Pradesh, which includes Meerut, Moradabad, Mathura etc., the staple food includes kachori, Aloo curry and the very regular dal, roti and rice. Petha is a very famous sweet dish available in the state and owes its authenticity to the city of Agra. Among the drinks, people prefer lassi, chaas and roohafza.

With one visit, you still cannot expect to experience the vibrant culture of Uttar Pradesh. Other than the rich cultural heritage, ancient temples, lavish palaces and monuments, Uttar Pradesh is home to a lot of architectural ruins- Taj Mahal being the most important one.
